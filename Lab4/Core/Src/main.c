void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim){

		if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1){ //control if there is interrupt in TIM5

			if(capture_flag == 0){ // if its first touch
				first_capture =  __HAL_TIM_GET_COUNTER(&htim5);
				capture_flag = 1;


			}
			else{

				second_capture = __HAL_TIM_GET_COUNTER(&htim5);

				if (second_capture >= first_capture) {
				    time_calc = second_capture - first_capture;
				} else {
				    time_calc = (0xFFFFFFFF - first_capture) + second_capture;
				}

				capture_flag = 0;
				led_display(time_calc);

			}

		}


}

void led_display(uint32_t time_difference){

	if(time_difference > 0000 && time_difference <= 2000){
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);//Green
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, GPIO_PIN_SET);//Blue
		HAL_Delay(500);
	}
	else if(time_difference > 2000 && time_difference <= 4000){
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, GPIO_PIN_SET);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_SET);//Red
		HAL_Delay(500);
	}
	else if(time_difference > 4000 && time_difference <= 6000){
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, GPIO_PIN_SET);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_SET);
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_SET);//Yellow
		HAL_Delay(500);
	}

}



